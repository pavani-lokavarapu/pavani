
public class assignmentoperator {

	public static void main(String[] args) {
		int x = 5;
		x += 4; // x = x + 5
		System.out.println(x);
        x -= 6; // x = x - 5
        System.out.println(x);
		x *= 7; // x = x * 5
		System.out.println(x);
		x /= 8; // x = x / 4
		System.out.println(x);
		x %= 5; // x = x % 5
		System.out.println(x);

	}

}
